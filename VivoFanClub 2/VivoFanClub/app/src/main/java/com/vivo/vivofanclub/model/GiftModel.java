package com.vivo.vivofanclub.model;

public class GiftModel {
    private String id, giftName, giftImage, gift_category;

    public GiftModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGiftName() {
        return giftName;
    }

    public void setGiftName(String giftName) {
        this.giftName = giftName;
    }

    public String getGiftImage() {
        return giftImage;
    }

    public void setGiftImage(String giftImage) {
        this.giftImage = giftImage;
    }

    public String getGift_category() {
        return gift_category;
    }

    public void setGift_category(String gift_category) {
        this.gift_category = gift_category;
    }
}
